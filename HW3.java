package com.company;

public class HW3 {
    public static void main(String[] args){
        System.out.println(Math.sqrt((81))); //вычисление квадратного корня
        System.out.println(Math.round(23.789));// округление числа
        System.out.println(Math.abs(-54));// абсолютная ценность аргумента. т.е. положительное значение
        System.out.println(Math.cbrt(64));// извлечение кубического корня
        System.out.println(Math.cos(45));// возвращает косинус 45
        System.out.println(Math.min(3,9));// минимум двух значений, переданных в аргументе
    }
}
